    private void CargarModelo(){
        try{
            modelo.addColumn("Nombre");
            modelo.addColumn("Género");
            modelo.addColumn("Edad");
            modelo.addColumn("Peso");
            modelo.addColumn("Estatura");
            modelo.addColumn("IMC");
            modelo.addColumn("Plan recomendado");
            TablaCalorico.setModel(modelo);
            CargarUsuario();
        }catch(IOException e){
            System.out.println(e);    
        }
    }
    private void CargarUsuario () throws IOException{
        String fila[];
        
        try{
            FileReader archivo = new FileReader ("SunshinelifeUsuarios1.txt");
            BufferedReader lectura = new BufferedReader(archivo);
            
            String linea = lectura.readLine(); 
            
            while(linea != null){
                fila=linea.split("/");
                modelo.addRow(fila);
                linea = lectura.readLine();
            }
            
        }catch(FileNotFoundException e){
            System.out.println(e);    
        }
    }

