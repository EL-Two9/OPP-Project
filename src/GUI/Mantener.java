    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int filaseleccionada = TablaCalorico.getSelectedRow();
        String genero = TablaCalorico.getValueAt(filaseleccionada, 1).toString();
        String peso = TablaCalorico.getValueAt(filaseleccionada, 3).toString();
        String altura = TablaCalorico.getValueAt(filaseleccionada, 4).toString();
        String edad = TablaCalorico.getValueAt(filaseleccionada, 2).toString();
        int peso1 = Integer.parseInt(peso);
        double alturaa = Double.parseDouble(altura);
        int edad1 = Integer.parseInt(edad);
        int altura1 =(int) (alturaa*100);
        /*System.out.print(altura1);*/
        int calorico = 0;
        if ("F".equals(genero)){
            calorico = (int) ((655+(9.6*peso1)+(1.8*altura1)-(4.7*edad1))*1.2);   
        }if("M".equals(genero)){
            calorico = (int) ((66+(13.7*peso1)+(5*altura1)-(6.8*edad1))*1.2);
        }
        String calorico1 = String.valueOf(calorico);
        txtcalorico.setText(calorico1);
    }//GEN-LAST:event_jButton1ActionPerformed

